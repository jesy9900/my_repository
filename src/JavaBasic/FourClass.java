package JavaBasic;

public class FourClass {
    public static void main(String[] args) {
        //byte 1 byte = whole number from -129 to 127
        byte x = 100;
        System.out.println(x);
        //short = 2bytes it hold -32, 768 to 32, 767
        short q = 6000;
        System.out.println(q);
        //int = 4bytes store whole number from -2, 147, 483, 648 to 2, 147, 483, 647
        int a=40000000;
        System.out.println(a);
        //long =8bytes store whole number from -9, 223, 372...807
        long k=223456789l;
        System.out.println(k);
        //primitive numeric floating point data type
       float f=10.10f;// if we want to use decimal point we have to use float. it holds 4bytes and
        //it is sufficient to hold 6 to 7 decimal numbers with round up
        System.out.println(f);
        //double = 8 bytes sufficient to hold 15 to 16 decimal digit number
        double d=1.9987654323456654321;
        System.out.println(d);
        //char = 2bytes store single character or letter/ascii values
        char c='e';
        System.out.println(c);
        //boolean true or false. (in computer language true means 1 and 0 means false)
        boolean bln=true;
        int n1= 100;
        int n2=200;
        System.out.println(n1>n2);


    }


    }

